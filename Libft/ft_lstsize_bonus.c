#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	**a;

	i = 0;
	a = &lst;
	if (lst)
	{
		i = 1;
		while ((*a)->next != 0)
		{
			i++;
			*a = (*a)->next;
		}
	}
	return (i);
}
