#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	if (s != NULL)
	{
		if (fd >= 0)
		{
			ft_putstr_fd(s, fd);
			ft_putchar_fd('\n', fd);
		}
	}
}
